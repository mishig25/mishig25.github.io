* [diffusers#559](https://github.com/huggingface/diffusers/pull/559): jax/flax implementation of Stable Diffusion 1.1.

* [tokenizers#890](https://github.com/huggingface/tokenizers/pull/890): improve serialization/deserialization of [tokenizers](https://github.com/huggingface/tokenizers) through Rust macros that implement necessary [serde](https://crates.io/crates/serde) traits.

* [huggingface/chat-ui](https://github.com/huggingface/chat-ui): UI for chatting with LLMs (used svelte, tailwindcss, mongodb, typescript). Supports tool calling (image generation) & RAG (websearch, document extraction).

* [transformers#13828](https://github.com/huggingface/transformers/pull/13828): image segmentation pipeline implementation for [facebook/detr-resnet-50](https://huggingface.co/facebook/detr-resnet-50) & other models that can do image segmentation.

* [tokenizers#976](https://github.com/huggingface/tokenizers/pull/976): parallelize [unigram tokenization](https://huggingface.co/learn/nlp-course/en/chapter6/7) trainer using [rayon](https://crates.io/crates/rayon), a popular Rust parallelization crate.

* [lerobot#277](https://github.com/huggingface/lerobot/pull/277): robotics dataset (videos & sensor signals) visualizer for easily testing & debugging real-life robitics. 

* [huggingface/doc-builder](https://github.com/huggingface/doc-builder): python package that parses markdown files + jupyter notebooks + python docstrings (through [inspect](https://docs.python.org/3/library/inspect.html)) and creates docs websites (using svelte), powering [hf.co/docs](https://huggingface.co/docs).

* [huggingface/gguf.js](https://github.com/huggingface/huggingface.js/tree/main/packages/gguf): js [GGUF](https://github.com/ggerganov/ggml/blob/master/docs/gguf.md) parser that works on remotely hosted files. GGUF is a weights file format, created by Georgi Gerganov (the creator of [llama.cpp](https://github.com/ggerganov/llama.cpp)).

* [diffuse-the-rest](https://x.com/mishig25/status/1563226161924407298): a web app that uses Stable Diffusion ([diffusers#559](https://github.com/huggingface/diffusers/pull/559) backend) to turn sketches into higher-quality images. One of the first apps to go viral that used Stable Diffusion (Aug 2022).

